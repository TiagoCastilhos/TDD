namespace Calculadora
{
    public class Calculadora
    {
        public int Soma(int a, int b)
        {
            return a + b;
        }

        public int Subtração(int a, int b)
        {
            return a - b;
        }

        public int Multiplicação(int a, int b)
        {
            return a * b;
        }
    }
}